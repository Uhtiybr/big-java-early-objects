package main.java.com.ch_01_introduction.e_01_09_mondrian_painting;

/**
 * Write a program that prints an imitation of a Piet Mondrian painting. Use character sequences such as
 * '@@@' or ':::' to indicate different colors, and use - and | to form lines.
 *
 * @author Alexander Parinov
 * @version 1.0
 * @since 27.06.2018
 */

public class MondrianPainting {
    public static void main(String[] args) {
        System.out.println("_________________|________|____");
        System.out.println("@@@@@@@@@@@@@@@@@|        |::::");
        System.out.println("@@@@@@@@@@@@@@@@@|        |::::");
        System.out.println("@@@@@@@@@@@@@@@@@|        |::::");
        System.out.println(":::::|    |::::::|        |::::");
        System.out.println(":::::|    |::::::|________|::::");
        System.out.println(":::::|    |::::::|@@@@@@@@|@@@@");
    }
}
