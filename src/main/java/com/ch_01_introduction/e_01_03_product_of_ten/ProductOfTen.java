package main.java.com.ch_01_introduction.e_01_03_product_of_ten;

/**
 * Write a program that prints the product of the first ten positive integers, 1 × 2 × … ×10.
 * (Use * to indicate multiplication in Java.)
 *
 * @author Alexander Parinov
 * @version 1.0
 * @since 26.06.2018
 */
public class ProductOfTen {
    public static void main(String[] args) {
        System.out.print("The sum of the first ten positive integers is: ");
        System.out.println(2 * 3 * 4 * 5 * 6 * 7 * 8 * 9 * 10); //no need to add 1*
    }

}
